function _M.get_country_currency(country_code)
  if utils.is_empty(country_code) == false and country_currency[country_code] then
    return country_currency[country_code]
  end
  return ''
end

function _M.get_default_lang(country_code)
  if utils.is_empty(country_code) == false and country_lang[country_code] then
    return country_lang[country_code]
  end
  return 'en'
end

function _M.get_default_direction(language_code)
  if utils.is_empty(language_code) == false and lang_rtl[language_code] then
    return 'rtl'
  end
  return 'ltr'
end

return _M
